/**
 * Establece la estructura básica para la posterior definición de
 * los requisitos de información y funcionales.
 * @author OnubaSoftware
 */
public class Requisito extends Generico
{
    /**
     * Nombre que tendrá el requisito.
     */
    private String nombre;
    /**
     * Descripción que tendrá el requisito.
     */
    private String descripcion;
    /**
     * Versión en la que está actualmente el requisito.
     */
    private String version;
    /**
     * Importancia del requisito en el proyecto.
     */
    private String importancia;
    /**
     * Urgencia del requisito en el proyecto.
     */
    private String urgencia;
    /**
     * Estado en el que se encuentra el requisito.
     */
    private String estado;
    /**
     * Estabilidad actual en el que se encuentra el requisito.
     */
    private String estabilidad;
    /**
     * Comentarios relacionados con el requisito.
     */
    private String comentario;
    /**
     * Dependencia del requisito.
     */
    private ArrayList<String> Dependencia;
    // <editor-fold defaultstate="collapsed" desc=" UML Marker "> 
    // #[regen=yes,id=DCE.87A79613-9DAA-9EBF-8E4A-2B34326590ED]
    // </editor-fold> 
    public Requisito ()
    {
        this.Dependencia=new ArrayList<String>();
    }

    /**
     * Obtiene el nombre del requisito.
     * @return Nombre del requisito.
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * Establece el nombre del requisito.
     * @param nombre Nombre del requisito.
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * Obtiene la descripción del requisito.
     * @return Descripción del requisito.
     */
    public String getDescripcion() {
        return descripcion;
    }

    /**
     * Establece la descripción del requisito.
     * @param descripcion Decripción del requisito.
     */
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    /**
     * Obtiene la versión en la que se encuentra el requisito.
     * @return Versión del requisito.
     */
    public String getVersion() {
        return version;
    }

    /**
     * Establece la versión en la que se encuentra el requisito.
     * @param version Versión del requisito.
     */
    public void setVersion(String version) {
        this.version = version;
    }

    /**
     * Obtiene la importancia del requisito en el proyecto.
     * @return Importancia del requisito.
     */
    public String getImportancia() {
        return importancia;
    }

    /**
     * Establece la importancia del requisito en el proyecto.
     * @param importancia Importancia del requisito.
     */
    public void setImportancia(String importancia) {
        this.importancia = importancia;
    }

    /**
     * Obtiene la urgencia del requisito en el proyecto.
     * @return Urgencia del requisito.
     */
    public String getUrgencia() {
        return urgencia;
    }

    /**
     * Establece la urgencia del requisito en el proyecto.
     * @param urgencia Urgencia del requisito.
     */
    public void setUrgencia(String urgencia) {
        this.urgencia = urgencia;
    }

    /**
     * Obtiene el estado del requisito.
     * @return Estado del requisito.
     */
    public String getEstado() {
        return estado;
    }

    /**
     * Establece el estado del requisito.
     * @param estado Estado del requisito.
     */
    public void setEstado(String estado) {
        this.estado = estado;
    }

    /**
     * Obtiene la estabilidad actual del requisito en el proyecto.
     * @return Estabilidad del requisito.
     */
    public String getEstabilidad() {
        return estabilidad;
    }

    /**
     * Establece la estabilidad actual del requisito en el proyecto.
     * @param estabilidad Estabilidad del requisito.
     */
    public void setEstabilidad(String estabilidad) {
        this.estabilidad = estabilidad;
    }

    /**
     * Obtiene los comentarios relacionados con el requisito.
     * @return Comentarios del requisito.
     */
    public String getComentario() {
        return comentario;
    }

    /**
     * Establece los comentarios relacionados con el requisito.
     * @param comentario Comentarios del requisito.
     */
    public void setComentario(String comentario) {
        this.comentario = comentario;
    }

    /**
     * Obtiene la lista de dependencias del requisito.
     * @return Lista de dependencias del requisito.
     */
    public ArrayList<String> getDependencia() {
        return Dependencia;
    }

    /**
     * Establece la lista de dependencias del requisito.
     * @param Dependencia Lista de dependencias del requisito.
     */
    public void setDependencia(ArrayList<String> Dependencia) {
        this.Dependencia = Dependencia;
    }

}

