package clases;

/**
 * Clase que define el elemento Parrafo
 * @author OnubaSotware
 */

public class Parrafo extends Generico 
{   /**
     * Contiene los atributos propios de Parrafo. Un String que contiene el nombre y un String que contiene su descripcion
     */
    private String Nombre;
    private String Descripcion;
    
    /**
     * Constructor de la clase, instancia al objeto.
     */
    public Parrafo(){
        
    }

    /**
     * Obtiene el nombre del parrafo
     * @return nombre de tipo String
     */
    public String getNombre() {
        return Nombre;
    }

    /**
     * Establece el nombre del parrafo
     * @param nombre String
     */
    public void setNombre(String nombre) {
        this.Nombre = nombre;
    }

    /**
     * Obtiene la descripcion del Parrafo
     * @return Descripcion de tipo String
     */
    public String getDescripcion() {
        return Descripcion;
    }

    /**
     * Establece la descripcion del Parrafo
     * @param Descripcion String
     */
    public void setDescripcion(String Descripcion) {
        this.Descripcion = Descripcion;
    }
    
    /**
     * Sobrecarga de ToString Para que el texto salga correctamente en el Jtextpane
     * @return Buff de tipo String
     */
    @Override
    public String toString()
    {
        return Nombre;
    }
}
